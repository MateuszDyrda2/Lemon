use serde::Deserialize;

#[derive(Deserialize, Debug)]
pub enum BasicTypes {
    Bool(bool),
    I8(i8),
    I16(i16),
    I32(i32),
    I64(i64),
    U8(u8),
    U16(u16),
    U32(u32),
    U64(u64),
    F32(f32),
    F64(f64),
    IVec2(i32, i32),
    IVec3(i32, i32, i32),
    IVec4(i32, i32, i32, i32),
    UVec2(u32, u32),
    UVec3(u32, u32, u32),
    UVec4(u32, u32, u32, u32),
    Vec2(f32, f32),
    Vec3(f32, f32, f32),
    Vec4(f32, f32, f32, f32),
    Mat2((f32, f32), (f32, f32)),
    Mat3((f32, f32, f32), (f32, f32, f32)),
    Mat4(
        (f32, f32, f32, f32),
        (f32, f32, f32, f32),
        (f32, f32, f32, f32),
    ),
    Str(String),
    HashString(u32),
    Asset(u32),
}
